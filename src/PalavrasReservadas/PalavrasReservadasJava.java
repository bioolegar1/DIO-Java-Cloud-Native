package PalavrasReservadas;

public class PalavrasReservadasJava {

    /*
    CONTROLE DE PACOTES
    - import: importa pacotes ou classes para dentro do código;
    - package: especifica a que pacote todas as classes de um arquivo pertencem;

    MODIFICADORES DE ACESSO
    - public: acesso de qualquer classe;
    - private: acesso apenas dentro da classe;
    - protected: acesso por classes no mesmo pacote e subclasses;

    PRIMITIVOS
    - boolean: um valor indicando verdadeiro ou falso;
    - byte: um itnteiro de 8 bits (signed);
    - char: um character unicode (16-bit unsigned);
    - double: um número de ponto flutuante de  64 bits (signed);
    - float: um número de ponto flutuante de  32 bits (signed);
    - int: um inteiro de  32 bits (signed);
    - long: um inteiro de  64 bits (signed);
    - short: um inteiro de  32 bits (signed);
    - void: indica que o méthodo não tem retorno de valor

    MODIFICADORES DE CLASSES, VARIAVEIS OU METHODOS
    - abstract: classe que não pode ser instanciada ou métodos que precisa ser implementado por uma subclasse não abstrata;
    - class: especifica uma classe;
    - extends: indica a superclasse que a subclasse está estendendo;
    - final: impossibilita que uma classe seja estendida, que um méthodo seja sobrescrito ou que uma variável seja reinicializada;
    - implements: indica as interfaces que uma classe irá implementar;
    - interface: especifica uma interface;
    - native: indica que um méthodo está escrito em uma linguagem depedendo de plataforma, com o C;
    - new instancia um novo objeto, chamando seu construtor;
    - static: faz um méthodo ou variável pertencer à classe ao invés de às instâncias;
    - strictfp: usado em frente a um méthodo ou classe para indicar que os números de ponto flutuante seguirão as regraas de ponto flutuante em todas as expressões;
    - synchronized: indica que um méthodo só pode ser acessado por uma thread de cada vez;
    - transient: impede a serialização de campos;
    - volatile: indica que uma variável pode ser alterada durante o uso de threads;


    CONTROLE DE FLUXO DENTRO DE UM BLOCO DE CÓDIGO
    - break: sai do bloco de código em que ele está;
    - case: executa um bloco de código dependendo do teste do switch;
    - continue: pula a execução do código que viria após essa linha e vai para a próxima passagem do loop;

    TRATAMENTO DE ERROS
    - catch: declara o bloco de código usado para tratar uma exceção;
    - assert: testa uma expressão condicional para verificar uma suposição do programador
    - finally: bloco de código, após um try-catch, que é executado independentemente do fluxo de programa seguindo ao lidar com uma exceção;
    - throw: usado para passar uma exceção para o méthodo que o chamou;
    - throws: indica que um méthodo pode passar uma exceção para o méthodo que o chamou;
    - try: bloco de código que tentará ser executado, mas que pode causar uma exceção;

    VARIÁVEIS DE REFERÊNCIA
    - super: refere-se a superclasse imediata;
    - this: refere-se a instância atual do objeto;

     */



}
